const PROMPT = ">> "

function start_repl()
    println(PRELUDE)
    while true
        print(PROMPT)
        line = readline()
        isempty(line) && break
        l = Lexer(line)
        token = next_token!(l)
        while token.type != EOF
            println(token)
            token = next_token!(l)
        end
    end
end
