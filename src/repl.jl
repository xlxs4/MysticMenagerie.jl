const PROMPT = ">> "

function start_repl()
    println(PRELUDE)
    while true
        print(PROMPT)
        line = readline()
        isempty(line) && break

        l = Lexer(line)
        p = Parser(l)
        program = parse_program!(p)

        if !isempty(p.errors)
            println(join(p.errors, "\n"))
            continue
        end

        println(program)
    end
end
